initial_balance = 30_000
interest = 7.5

first_quarter = initial_balance * (1 + interest / 100 * 0.25)
second_querter = initial_balance * (1 + interest / 100 * 0.50)
third_querter = initial_balance * (1 + interest / 100 * 0.75)
fourth_querter = initial_balance * (1 + interest / 100)

print("Po 1 kwartale zysk wyniesie: " +str(round(first_quarter)) + " złotych.")
print("Po 2 kwartale zysk wyniesie: " +str(round(second_querter)) + " złotych.")
print("Po 3 kwartale zysk wyniesie: " +str(round(third_querter)) + " złotych.")
print("Po 4 kwartale zysk wyniesie: " +str(round(fourth_querter)) + " złotych.")
print("Roczny zysk z lokaty wyniesie: " + str(round(fourth_querter - initial_balance)) + " złotych.")


